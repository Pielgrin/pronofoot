<h2>Règlement du concours COUPE DU MONDE</h2>
<?php // include/require ?>
<p>
<span class="strong">1. <span class="underline">L'inscription est fixée à 5€</span></span>. Si quelqu'un souhaite participer alors que la compétition est déjà commencée, l'entrée est toujours à 5€ et il n'y a pas de compensation pour les matchs perdus.<br />
</p>
<p>
<span class="strong">2.</span> Pour les phases finales, le score retenu est celui qui est en vigueur à la <span class="strong underline">fin du temps de jeu</span> (90 minutes en temps normal, 120 minutes si prolongations). On peut donc pronostiquer un match nul en pariant sur d'éventuels tirs au but.
</p>
<p>
<span class="strong">3.</span> Tous les matchs de la coupe du monde seront pronostiquables dès le début de la compétition, y compris les phases finales dont les équipes seront définies le moment venu. <span class="strong underline">Il est possible de modifier les pronostics d'une journée</span> autant de fois qu'on le veut jusqu'à ce qu'elle commence.<br />
</p>
<p>
<span class="strong">4.</span> Pour des raisons techniques historiques, le blocage des pronostics se fait <span class="strong underline">dès le premier match d'une journée</span>. Donc si le 11/06 il y a un match à 14h et un à 20h, tous les pronostics seront bloqués dès 14h. Si besoin, un système de rappel automatique par email est activable (section "mon profil" dans le panel à droite).<br />
</p>
<p>
<span class="strong">5.</span> En cas de problème technique pour pronostiquer (site inaccessible, matchs indisponibles...), vous pouvez envoyer vos pronostics par mail à l'adresse <a href="mailto:pronofoot@julienp.fr">pronofoot@julienp.fr</a>, l'heure d'envoi faisant foi (cf. numéro 4).<br />
Pour éviter tout abus, merci de joindre à votre mail une capture d'écran qui prouve qu'il y a eu un problème. Sans cela les pronostics seront refusés. 
</p>
<p>
<span class="strong">6.</span> L'attribution des points se fait selon le barême suivant : 1 point pour une bonne tendance (match nul / bon gagnant), 3 points pour un bon score (score exact), et 0 point sinon.<br />
	Exemple :<br />
	Pour le match France-Brésil, avec un pronostic de 3-0<br />
	<span class="go">0 point s'il y a match nul</span><br />
	<span class="go">0 point si le Brésil gagne</span><br />
	<span class="go">1 point si la France gagne avec un score différent de 3-0</span><br />
	<span class="go">3 points si la France gagne 3-0</span><br />
</p>
<p>
<span class="strong">7.</span> Par défaut, le classement est ordonné par nombre de points gagnés. Un classement par moyenne est disponible mais il est purement indicatif : <span class="strong underline">le concours ne porte que sur les points effectifs</span>
</p>
<p>
<span class="strong">8.</span> La répartition de la cagnotte est "souple" et récompense jusqu'au 5ème meilleur pronostiqueur. La répartition a été calculée sur la base théorique suivante :<br />
	Pour <span class="strong">100€</span> dans la cagnotte, les gains sont<br />
		1er : <span class="strong">50€</span><br />
		2ème : <span class="strong">25€</span><br />
		3ème : <span class="strong">15€</span><br />
		4ème : <span class="strong">5€</span><br />
		5ème : <span class="strong">5€</span><br />
</p>
<p>
<span class="strong">9.</span> Si il y a égalité entre plusieurs joueurs du haut du tableau, on fait la somme des gains de leur position et de la suivante puis on divise par 2<br />
	Exemple pour deux premiers ex-aequo :<br />
	50 + 25 = 75€<br />
	75/2 = 37,5€ chacun<br />
</p>
