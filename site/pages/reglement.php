<h2>Règlement du jeu</h2>
<?php // include/require ?>
<p>
<span class="strong">1.</span> Les joueurs pronostiquent chaque semaine quand il y a une journée de ligue 1, tout ce qui arrive après est fait de manière automatique (calcul du nombre de points gagnés, mise à jour du classement...)<br />
</p>
<p>
<span class="strong">2.</span> <span class="strong underline">Il est possible de modifier les pronostics d'une journée</span> autant de fois qu'on le veut jusqu'à ce qu'elle commence.<br />
</p>
<p>
<span class="strong">3.</span> Pour des raisons techniques historiques, le blocage des pronostics se fait <span class="strong underline">dès le premier match d'une journée</span>. Donc si le 11/06 il y a un match à 14h et un à 20h, tous les pronostics seront bloqués dès 14h. Si besoin, un système de rappel automatique par email est activable (section "mon profil" dans le panel à droite).<br />
</p>
<p>
<span class="strong">4.</span> En cas de problème technique pour pronostiquer (site inaccessible, matchs indisponibles...), vous pouvez envoyer vos pronostics par mail à l'adresse <a href="mailto:pronofoot@julienp.fr">pronofoot@julienp.fr</a>, l'heure d'envoi faisant foi (cf. numéro 4).<br />
Pour éviter tout abus, merci de joindre à votre mail une capture d'écran qui prouve qu'il y a eu un problème. Sans cela les pronostics seront refusés. 
</p>
<p>
<span class="strong">5.</span> L'attribution des points se fait selon le barême suivant : 1 point pour une bonne tendance (match nul / bon gagnant), 3 points pour un bon score (score exact), et 0 point sinon.<br />
	Exemple :<br />
	Pour le match France-Brésil, avec un pronostic de 3-0<br />
	<span class="go">0 point s'il y a match nul</span><br />
	<span class="go">0 point si le Brésil gagne</span><br />
	<span class="go">1 point si la France gagne avec un score différent de 3-0</span><br />
	<span class="go">3 points si la France gagne 3-0</span><br />
</p>
